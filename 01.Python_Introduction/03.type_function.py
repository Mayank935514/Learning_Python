# type() function--> It is a built-in function that tells you a datatype of a variale or value
# Datatype--> Datatypes are associated with actual objects and we just can create variable, which refers to those objects.

# Example-1
a=10 # type of variable which this variable refers to
print(type(a))

b=10.5
print(type(b))

c=2+4j
print(type(c))

a=True
print(type(a))

# Example-2
# Program for Sequence type
str="Ravi"
print(type(str))

l=[10,20,30]
print(type(l))

t=(10,20,30)
print(type(t))

s={10,20,30}
print(type(s))

d={10: "gmp", 20: "idle"}
print(type(d))